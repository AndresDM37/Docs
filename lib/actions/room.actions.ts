export const updateDocument = async (roomId: string, title: string) => {
  try {
    const updateRoom = await liveblocks.updateRoom(roomId, {
      metadata: {
        title,
      },
    });

    revalidatePath(`document/${roomId}`);

    return parseStringify(updateRoom);
  } catch (error) {
    console.error(error);
  }
};

